        if (scaleWidth.getText().equals(scaleWidthHolder.getTextoHolder()) || scaleHeigth.getText().equals(scaleHeigthHolder.getTextoHolder())) {
            return;
        }

        if (scaleWidth.getText().isEmpty() || scaleHeigth.getText().isEmpty()) {
            return;
        }

